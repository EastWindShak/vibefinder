    def _build_clap_context(self, request: RecommendationRequest) -> str:
        """Build a text description from CLAP analysis for LLM."""
        parts = []
        
        if request.audio_mood_tags:
            parts.append(f"Mood: {', '.join(request.audio_mood_tags)}")
        if request.audio_genre_tags:
            parts.append(f"Genre: {', '.join(request.audio_genre_tags)}")
        if request.audio_tempo:
            parts.append(f"Tempo: {request.audio_tempo}")
        if request.audio_energy:
            parts.append(f"Energy: {request.audio_energy}")
        
        return f"Find music matching this audio analysis: {'. '.join(parts)}"
    
    def _build_queries_from_clap_tags(self, request: RecommendationRequest) -> List[str]:
        """Build search queries from CLAP tags when pre-generated queries not available."""
        queries = []
        
        mood_tags = request.audio_mood_tags or []
        genre_tags = request.audio_genre_tags or []
        
        # Mood + genre combinations
        for mood in mood_tags[:3]:
            queries.append(f"{mood} music")
            for genre in genre_tags[:2]:
                queries.append(f"{mood} {genre}")
        
        # Genre-only queries
        for genre in genre_tags[:2]:
            queries.append(f"{genre} songs")
        
        # Tempo/energy-based
        if request.audio_tempo == "slow" and request.audio_energy == "low":
            queries.extend(["slow relaxing music", "chill ballads"])
        elif request.audio_tempo == "fast" and request.audio_energy == "high":
            queries.extend(["upbeat energetic music", "party songs"])
        
        return queries[:8]
    
    async def _search_youtube_with_queries(
        self,
        search_queries: List[str],
        youtube_auth_headers: Optional[str],
        request: RecommendationRequest,
        trace: Optional[Any] = None
    ) -> List[EnrichedRecommendation]:
        """Search YouTube Music with a list of queries and return enriched results."""
        enriched = []
        # Include previous songs to avoid duplicates on "Load More"
        seen_video_ids = set(request.previous_songs or [])
        
        yt_span = None
        if trace:
            try:
                yt_span = trace.span(
                    name="youtube_music_search",
                    input={"queries": search_queries}
                )
            except Exception:
                pass
        
        for query in search_queries:
            try:
                results = await self.mcp_client.search_songs(
                    query, limit=6, auth_headers=youtube_auth_headers
                )
                for song in results:
                    if song.video_id and song.video_id not in seen_video_ids:
                        seen_video_ids.add(song.video_id)
                        
                        # Build reason from CLAP data
                        mood_str = ", ".join(request.audio_mood_tags[:2]) if request.audio_mood_tags else None
                        
                        enriched.append(EnrichedRecommendation(
                            title=song.title,
                            artist=song.artist,
                            reason=f"Matches: {mood_str}" if mood_str else f"Matches: {query}",
                            genre=request.audio_genre_tags[0] if request.audio_genre_tags else None,
                            mood=mood_str,
                            video_id=song.video_id,
                            album=song.album,
                            duration=song.duration,
                            thumbnail_url=song.thumbnail_url
                        ))
            except Exception as e:
                logger.warning(f"YouTube search failed for '{query}': {e}")
        
        if yt_span:
            try:
                yt_span.end(output={"songs_found": len(enriched)})
            except Exception:
                pass
        
        return enriched
    
    async def _filter_enriched_dislikes(
        self,
        user_id: uuid.UUID,
        enriched: List[EnrichedRecommendation]
    ) -> tuple[List[EnrichedRecommendation], int, int]:
        """
        Filter and deprioritize disliked songs from enriched recommendations.
        
        1. REMOVE exact matches (same video_id or same title+artist)
        2. DEPRIORITIZE songs similar to dislikes (move to end of list)
        
        Returns:
            Tuple of (filtered_recommendations, removed_count, deprioritized_count)
        """
        # Step 1: Remove exact disliked songs
        filtered = []
        filtered_count = 0
        
        for rec in enriched:
            is_disliked = await self.chromadb.check_if_disliked(
                user_id,
                rec.title,
                rec.artist,
                rec.video_id
            )
            if is_disliked:
                filtered_count += 1
            else:
                filtered.append(rec)
        
        # Step 2: Deprioritize songs similar to dislikes
        if filtered:
            filtered, deprioritized_count = await self._deprioritize_similar_to_dislikes(
                user_id, filtered
            )
        else:
            deprioritized_count = 0
        
        return filtered, filtered_count, deprioritized_count
    
    async def _get_recommendations_from_ollama(
        self,
        request: RecommendationRequest,
        user_id: Optional[uuid.UUID],
        youtube_auth_headers: Optional[str],
        session_id: str
    ) -> RecommendationResponse:
        """
        Get recommendations using Ollama + YouTube Music hybrid approach.
        
        Ollama interprets the user's mood/text and generates search queries,
        then YouTube Music finds real songs matching those queries.
        
        For logged-in users: includes ChromaDB preferences in the LLM prompt.
        """
        import logging
        logger = logging.getLogger(__name__)
        
        # Track ChromaDB usage
        used_chromadb = False
        chromadb_info = None
        filtered_count = 0
        
        # Get the user's text query
        user_text = request.mood_text or ""
        if not user_text and request.song_title:
            user_text = f"songs similar to {request.song_title} by {request.song_artist}"
        
        if not user_text:
            # No text provided, return empty
            return RecommendationResponse(
                recommendations=[],
                input_type=request.input_type,
                session_id=session_id,
                is_continuation=False,
                used_chromadb=False
            )
        
        # Start Langfuse trace for mood-based recommendations
        trace = None
        if self.langfuse:
            try:
                trace = self.langfuse.trace(
                    name="recommendations_from_mood",
                    input={
                        "mood_text": user_text,
                        "session_id": session_id
                    },
                    metadata={
                        "flow": "Case 1 - Mood/Text Based",
                        "user_id": str(user_id) if user_id else "guest",
                        "has_previous_songs": bool(request.previous_songs)
                    }
                )
            except Exception as e:
                logger.warning(f"Langfuse trace error: {e}")
        
        try:
            # Step 1: Get user preferences from ChromaDB (for logged-in users)
            system_prompt = None
            if user_id:
                logger.info(f"[MOOD] Getting preferences for user_id: {user_id}")
                
                # Log preferences fetch to Langfuse
                preferences_span = None
                if trace:
                    try:
                        preferences_span = trace.span(
                            name="get_chromadb_preferences",
                            input={"user_id": str(user_id)}
                        )
                    except Exception:
                        pass
                
                system_prompt, has_preferences = await self._build_system_prompt(user_id)
                logger.info(f"[MOOD] has_preferences: {has_preferences}")
                
                if preferences_span:
                    try:
                        preferences_span.end(output={"has_preferences": has_preferences})
                    except Exception:
                        pass
                
                if has_preferences:
                    used_chromadb = True
                    chromadb_info = "Used preferences for personalized recommendations"
                    logger.info(f"[MOOD] Using ChromaDB preferences for user {user_id}")
                else:
                    logger.info(f"[MOOD] No preferences found for user {user_id}")
            
            # Step 2: Ollama extracts search queries from user's text (with preferences if available)
            logger.info(f"Extracting search queries from: {user_text}")
            
            # Log LLM call to Langfuse
            llm_span = None
            if trace:
                try:
                    llm_span = trace.span(
                        name="llm_extract_search_queries",
                        input={
                            "user_text": user_text,
                            "has_system_prompt": system_prompt is not None
                        }
                    )
                except Exception:
                    pass
            
            search_queries = await self.ollama.extract_search_queries(
                user_text,
                system_prompt=system_prompt
            )
            logger.info(f"Generated {len(search_queries)} search queries: {search_queries}")
            
            if llm_span:
                try:
                    llm_span.end(output={"queries": search_queries})
                except Exception:
                    pass
            
            # Step 2: Search YouTube Music for each query
            all_songs: List[YouTubeMusicSong] = []
            # Include previous songs to avoid duplicates on "Load More"
            seen_video_ids = set(request.previous_songs or [])
            
            for query in search_queries:
                try:
                    results = await self.mcp_client.search_songs(
                        query, 
                        limit=6, 
                        auth_headers=youtube_auth_headers
                    )
                    for song in results:
                        # Deduplicate by video_id
                        if song.video_id and song.video_id not in seen_video_ids:
                            seen_video_ids.add(song.video_id)
                            all_songs.append(song)
                except Exception as e:
                    logger.warning(f"Search failed for query '{query}': {e}")
                    continue
            
            logger.info(f"Found {len(all_songs)} unique songs from YouTube Music")
            
            # Convert to enriched recommendations
            enriched = [
                EnrichedRecommendation(
                    title=song.title,
                    artist=song.artist,
                    reason=f"Matches: {user_text[:50]}...",
                    genre=None,
                    mood=None,
                    video_id=song.video_id,
                    album=song.album,
                    duration=song.duration,
                    thumbnail_url=song.thumbnail_url
                )
                for song in all_songs
            ]
            
            # Filter dislikes for registered users
            if user_id and enriched:
                enriched, filtered_count, _ = await self._filter_enriched_dislikes(
                    user_id, enriched
                )
                if filtered_count > 0:
                    used_chromadb = True
                    if chromadb_info:
                        chromadb_info = f"{chromadb_info} | Blocked {filtered_count} disliked"
                    else:
                        chromadb_info = f"Blocked {filtered_count} disliked songs"
            
            # Log final output to Langfuse
            if trace:
                try:
                    trace.update(output={
                        "recommendations_count": len(enriched[:RECOMMENDATIONS_PER_REQUEST]),
                        "used_chromadb": used_chromadb,
                        "search_queries": search_queries,
                        "recommendations": [
                            {"title": r.title, "artist": r.artist, "video_id": r.video_id}
                            for r in enriched[:RECOMMENDATIONS_PER_REQUEST]
                        ]
                    })
                    self.langfuse.flush()
                except Exception as e:
                    logger.warning(f"Langfuse output error: {e}")
            
            return RecommendationResponse(
                recommendations=enriched[:RECOMMENDATIONS_PER_REQUEST],
                input_type=request.input_type,
                session_id=session_id,
                is_continuation=bool(request.previous_songs),
                used_chromadb=used_chromadb,
                chromadb_info=chromadb_info
            )
            
        except Exception as e:
            logger.error(f"Hybrid recommendation failed: {e}")
            if trace:
                try:
                    trace.update(output={"error": str(e)}, level="ERROR")
                    self.langfuse.flush()
                except Exception:
                    pass
            # Fallback: direct YouTube Music search with user text
            try:
                results = await self.mcp_client.search_songs(
                    user_text, 
                    limit=10, 
                    auth_headers=youtube_auth_headers
                )
                enriched = [
                    EnrichedRecommendation(
                        title=song.title,
                        artist=song.artist,
                        reason="Direct search result",
                        video_id=song.video_id,
                        album=song.album,
                        duration=song.duration,
                        thumbnail_url=song.thumbnail_url
                    )
                    for song in results
                ]
                return RecommendationResponse(
                    recommendations=enriched,
                    input_type=request.input_type,
                    session_id=session_id,
                    is_continuation=False,
                    used_chromadb=False
                )
            except Exception:
                return RecommendationResponse(
                    recommendations=[],
                    input_type=request.input_type,
                    session_id=session_id,
                    is_continuation=False,
                    used_chromadb=False
                )
    
    async def _build_system_prompt(self, user_id: Optional[uuid.UUID]) -> tuple[str, bool]:
        """
        Build the system prompt based on user type.
        
        For registered users: includes preference context from ChromaDB
        For guests: generic discovery-focused prompt
        
        Returns:
            Tuple of (system_prompt, used_chromadb)
        """
        if user_id is None:
            # Guest user - no history
            return PromptTemplates.guest_user_prompt(), False
        
        # Registered user - get preferences
        try:
            logger.info(f"[BUILD_PROMPT] Fetching preferences for user_id: {user_id}")
            preferences = await self.chromadb.get_preference_summary(user_id)
            logger.info(f"[BUILD_PROMPT] preferences: {preferences}")
            dislikes = await self.chromadb.get_user_dislikes(user_id)
            logger.info(f"[BUILD_PROMPT] dislikes count: {len(dislikes)}")
            
            dislike_list = [
                f"{d.get('metadata', {}).get('song_title', '')} - {d.get('metadata', {}).get('artist', '')}"
                for d in dislikes
            ]
            
            # Check if we actually have preferences data (not just empty dict)
            has_preferences = (
                preferences.get("total_likes", 0) > 0 or 
                preferences.get("total_dislikes", 0) > 0 or 
                bool(dislike_list)
            )
            logger.info(f"[BUILD_PROMPT] has_preferences: {has_preferences} (likes={preferences.get('total_likes', 0)}, dislikes={preferences.get('total_dislikes', 0)})")
            
            return PromptTemplates.registered_user_prompt(preferences, dislike_list), has_preferences
        except Exception as e:
            # Fallback to guest prompt on error
            logger.error(f"[BUILD_PROMPT] Error getting preferences: {e}")
            return PromptTemplates.guest_user_prompt(), False
    
    def _build_user_prompt(self, request: RecommendationRequest) -> str:
        """Build the user prompt based on input type."""
        if request.input_type == "mood":
            return PromptTemplates.build_user_prompt_from_mood(
                request.mood_text or "upbeat and energetic"
            )
        elif request.input_type == "combined":
            # Combined prompt with both mood and song context
            return PromptTemplates.build_combined_prompt(
                mood=request.mood_text or "",
                title=request.song_title or "Unknown",
                artist=request.song_artist or "Unknown",
                genre=request.song_genre
            )
        else:  # audio
            return PromptTemplates.build_user_prompt_from_song(
                title=request.song_title or "Unknown",
                artist=request.song_artist or "Unknown",
                genre=request.song_genre
            )
    
    async def _deprioritize_similar_to_dislikes(
        self,
        user_id: uuid.UUID,
        recommendations: List[EnrichedRecommendation]
    ) -> tuple[List[EnrichedRecommendation], int]:
        """
        Deprioritize (but don't remove) songs similar to dislikes.
        
        Songs similar to dislikes are moved to the end of the list,
        but still shown. This allows discovery while respecting preferences.
        
        Returns:
            Tuple of (reordered recommendations, count of deprioritized songs)
        """
        if not recommendations:
            return recommendations, 0
        
        # Get similarity penalty scores
        songs_data = [
            {"title": rec.title, "artist": rec.artist}
            for rec in recommendations
        ]
        
        penalty_scores = await self.chromadb.get_dislike_similarity_scores(
            user_id,
            songs_data
        )
        
        # Pair recommendations with their penalty scores
        scored_recs = list(zip(recommendations, penalty_scores))
        
        # Count how many have significant penalties (> 0.2)
        deprioritized_count = sum(1 for _, score in scored_recs if score > 0.2)
        
        # Sort by penalty score (lowest penalty first = highest priority)
        scored_recs.sort(key=lambda x: x[1])
        
        # Extract sorted recommendations
        sorted_recs = [rec for rec, _ in scored_recs]
        
        return sorted_recs, deprioritized_count
    
    async def _enrich_with_youtube(
        self,
        recommendations: List[SongRecommendation],
        auth_headers: Optional[str]
    ) -> List[EnrichedRecommendation]:
        """
        Enrich recommendations with YouTube Music data.
        
        Searches for each song on YouTube Music and adds video IDs,
        thumbnails, and other metadata.
        """
        enriched = []
        
        # Prepare songs for batch search
        songs_to_search = [
            {"title": rec.title, "artist": rec.artist}
            for rec in recommendations
        ]
        
        # Get YouTube Music data
        try:
            youtube_songs = await self.mcp_client.enrich_songs_with_youtube_data(
                songs_to_search,
                auth_headers
            )
            
            # Combine LLM recommendations with YouTube data
            for i, rec in enumerate(recommendations):
                yt_song = youtube_songs[i] if i < len(youtube_songs) else None
                enriched.append(
                    EnrichedRecommendation.from_recommendation_and_youtube(rec, yt_song)
                )
        except Exception:
            # On error, return recommendations without YouTube data
            for rec in recommendations:
                enriched.append(
                    EnrichedRecommendation(
                        title=rec.title,
                        artist=rec.artist,
                        reason=rec.reason,
                        genre=rec.genre,
                        mood=rec.mood
                    )
                )
        
        return enriched
    
    async def record_feedback(
        self,
        user_id: uuid.UUID,
        song_title: str,
        artist: str,
        feedback_score: int,
        video_id: Optional[str] = None,
        genre: Optional[str] = None,
        mood_tags: Optional[List[str]] = None
    ) -> bool:
        """
        Record user feedback on a recommendation.
        
        Updates ChromaDB with the user's like/dislike for future
        recommendation personalization.
        
        Args:
            user_id: The user's UUID
            song_title: Title of the song
            artist: Artist name
            feedback_score: 1 for like, -1 for dislike
            video_id: Optional YouTube video ID
            genre: Optional genre
            mood_tags: Optional mood/vibe tags
            
        Returns:
            True if successful
        """
        from app.db.chromadb_client import MusicPreference
        
        preference = MusicPreference(
            song_title=song_title,
            artist=artist,
            genre=genre,
            mood_tags=mood_tags,
            feedback_score=feedback_score,
            youtube_video_id=video_id
        )
        
        try:
            await self.chromadb.add_preference(user_id, preference)
            return True
        except Exception:
            return False


